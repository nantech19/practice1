package com.nandu.pro.javapractice1.model;

public class Plane {

    private String name;
    private String id;

    public Plane(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return id;
    }
}
